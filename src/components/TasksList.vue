<template>
  <transition-group name="task">
    <Task v-for="task in tasksData" :task="task" :key="task.id" />
  </transition-group>
</template>

<script>
import Task from './Task.vue'

export default {
  components: {
    Task
  },
  emits: [
    'update:tasks'
  ],
  props: {
    tasks: Array
  },
  data: () => ({
    tasksData: []
  }),
  watch: {
    tasksData: {
      deep: true,
      handler (val) {
        this.$emit('update:tasks', val)
      }
    },
    tasks: {
      deep: true,
      handler (val) {
        this.tasksData = val
      }
    }
  },
  mounted () {
    if (this.tasks) {
      this.tasksData = this.tasks
    }
  }
}
</script>

<style lang="scss">
@use '../assets/scss' as r;

.task {
  $task-height: 43.375px;

  &-enter-active, &-leave-active {
    transition: .3s r.$easing;
  }

  &-enter-from {
    transform: translateY($task-height);
    opacity: 0;
    height: 0;
  }

  &-enter-to {
    height: $task-height;
  }

  &-leave-from {
    height: $task-height;
  }

  &-leave-to {
    transform: translateY($task-height);
    height: 0;
    opacity: 0;
  }
}
</style>
